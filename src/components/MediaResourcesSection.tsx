      const link = document.createElement("a");
      link.href = url;
      link.download = selectedDownload ? `${selectedDownload.id}_guide_preview.txt` : "pius_owolawi_framework.txt";
      document.body.appendChild(link);
      link.click();
      document.body.removeChild(link);

