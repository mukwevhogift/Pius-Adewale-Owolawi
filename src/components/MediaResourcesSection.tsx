import { 
  FileText, 
  Download, 
  Video, 
  Radio, 
  Sparkles, 
  BookOpen, 
  Send, 
  X, 
  CheckCircle,
  Clock,
  Compass,
  ArrowRight
} from "lucide-react";
import { DOWNLOADABLE_RESOURCES } from "../data.js";

